CREATE TABLE chapter (
  id BIGINT PRIMARY KEY,
  title VARCHAR(128) NOT NULL,
  description VARCHAR(512) NOT NULL,
  sort_order INT NOT NULL,
  UNIQUE KEY uk_chapter_title (title)
);

CREATE TABLE knowledge_point (
  id BIGINT PRIMARY KEY,
  chapter_id BIGINT NOT NULL,
  title VARCHAR(128) NOT NULL,
  summary VARCHAR(1024) NOT NULL,
  difficulty VARCHAR(16) NOT NULL,
  tags_json JSON NOT NULL,
  INDEX idx_kp_chapter (chapter_id),
  UNIQUE KEY uk_kp_title (title)
);

CREATE TABLE practice_question (
  id BIGINT PRIMARY KEY,
  knowledge_point_id BIGINT NOT NULL,
  stem VARCHAR(1024) NOT NULL,
  answer_key VARCHAR(8) NOT NULL,
  explanation VARCHAR(1024) NOT NULL,
  INDEX idx_question_kp (knowledge_point_id)
);

CREATE TABLE question_option (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  question_id BIGINT NOT NULL,
  option_key VARCHAR(8) NOT NULL,
  option_text VARCHAR(512) NOT NULL,
  sort_order INT NOT NULL,
  UNIQUE KEY uk_question_option_key (question_id, option_key),
  INDEX idx_option_question_sort (question_id, sort_order)
);

